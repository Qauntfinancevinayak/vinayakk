    "    return data\n",
    "\n",
    "# Fetch live data\n",
    "try:\n",
    "    live_data = fetch_prices(list(portfolio_weights.keys()))\n",
    "    if live_data.empty:\n",
    "        st.error(\"No data fetched. Please check the ticker symbols or your network connection.\")\n",
    "    else:\n",
    "        st.success(\"Data fetched successfully!\")\n",

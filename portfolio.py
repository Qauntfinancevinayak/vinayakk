    "    st.error(f\"Error fetching data: {e}\")\n",
    "\n",
    "# Calculate allocation and number of shares for each stock\n",
    "def calculate_allocation(live_data, total_investment, portfolio_weights):\n",
    "    allocation = {}\n",
    "    for ticker, weight in portfolio_weights.items():\n",
    "        if ticker in live_data.columns:\n",
    "            allocated_amount = total_investment * weight\n",
    "            current_price = live_data[ticker].iloc[-1]\n",
    "            shares = allocated_amount // current_price if current_price > 0 else 0\n",
    "            allocation[ticker] = {\n",
    "                'allocated_amount': allocated_amount,\n",
    "                'current_price': current_price,\n",
    "                'shares': shares,\n",
    "                'investment': shares * current_price\n",
    "            }\n",
    "    return allocation\n",
    "\n",
    "if not live_data.empty:\n",
    "    allocation = calculate_allocation(live_data, initial_investment, portfolio_weights)\n",
    "\n",
